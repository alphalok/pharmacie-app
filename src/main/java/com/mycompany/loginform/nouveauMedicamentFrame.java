    private void ajouterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterBtnActionPerformed

    String medicamentName=NvMedicNameField.getText();
    String medicamentQuanntite=NvQuantiteFiled.getText();
    String medicamentPrix=NvPrixFiled.getText();
    
    if(medicamentName.isEmpty()||medicamentPrix.isEmpty()||medicamentQuanntite.isEmpty()){
        JOptionPane.showMessageDialog(null, "Veuillez entrez tout les information!!");
    }else{
        PreparedStatement ps;
        String query ="INSERT INTO `medicament`(`NAME`, `QUANTITE`, `PRIX`) VALUES (?,?,?)";
        try {
            ps= MyConnection.getConnection().prepareStatement(query);
               
               ps.setString(1, medicamentName);
               ps.setString(2, medicamentQuanntite);
               ps.setString(3, medicamentPrix);
               
               
               if(ps.executeUpdate()>0){
                   JOptionPane.showMessageDialog(null, "Medicament added");
               }
        } catch (Exception e) {
        }

        
    }
    

    }//GEN-LAST:event_ajouterBtnActionPerformed

